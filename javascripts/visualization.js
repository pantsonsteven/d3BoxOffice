// Sets up SVG space
function buildSVG(data) {

   var movies = createData(data);

   var margin  = {
      top      : 100,
      right    : 100,
      bottom   : 100,
      left     : 150
      },
      width    = 1300 - margin.right - margin.left,
      height   = 750 - margin.top - margin.bottom;

   var maxDate = d3.max(movies, function(m) {
      return d3.max(m.values, function(d) {
         return d.date;
      });
   });

   var minDate = d3.min(movies, function(m) {
      return d3.min(m.values, function(d) {
         return d.date;
      });
   });

   var maxSales = d3.max(movies, function(m) {
      return m.maxSales;
   });

   var minSales = d3.min(movies, function(m) {
      return m.minSales;
   });

   window.x = d3.time.scale()
      .range([0, width])
      .domain([minDate, maxDate]);

   window.y = d3.scale.linear()
      .range([height, 0])
      .domain([0, maxSales]);

   var xAxis = d3.svg.axis()
      .scale(window.x)
      .orient('bottom');

   var yAxis = d3.svg.axis()
      .scale(window.y)
      .orient('left');

   window.svg = d3.select('body').append('svg')
         .attr('width', width + margin.left + margin.right)
         .attr('height', height + margin.top + margin.bottom)
      .append('g')
         .attr('width', width)
         .attr('height', height)
         .attr('class', 'graph')
         .attr('transform', 'translate(' + margin.left + ',' + margin.top + ')')
      
   window.svg.append('g')
      .attr('class', 'x axis')
      .attr('transform', 'translate(0,'+height+')')
      .call(xAxis)
      .append('text')
      .text('Date')
      .attr('x', width/2)
      .attr('y', 50);

   window.svg.append('g')
      .attr('class', 'y axis')
      .call(yAxis)
      .append('text')
      .text('Sales')
      .attr('x', -100)
      .attr('y', height/2);

   // Creates ordinal color scale
   window.color = d3.scale.ordinal()
      .range(colorbrewer.Paired[12]);

   project(movies);

}; // End of buildSVG



